"""
실습 20: 입장 판정

나이와 보호자 동반 여부를 입력받아,
나이가 20세 이상이거나 보호자가 동반된 경우 "입장 가능"을,
그렇지 않으면 "입장 불가"를 출력하세요.

힌트: 비교 연산자(>=)와 논리 연산자(or)를 조합합니다.

참고: 조건에 따라 다른 결과를 출력하려면 if/else를 사용합니다.
      if 조건:
          참일 때 실행
      else:
          거짓일 때 실행
"""

age = int(input("나이를 입력하세요: "))
guardian = input("보호자 동반 여부 (y/n): ")

# 아래에 입장 가능 여부를 판별하여 출력하세요

# 나이가 20세 이상이거나 보호자가 동반된 경우 "입장가능"
if age >= 20 or guardian == "y":
    print("입장 가능")
# 그렇지 않으면 "입장 불가"
else :
    print("입장 불가")

