"""
글로벌시스템융합과 프로그래밍(1) 실습 문제

실습 9: 혈액형 성격

혈액형(A, B, O, AB)을 입력받아 성격 설명을 출력하세요.
if/elif/else를 사용합니다.

[성격 설명]
A형: 꼼꼼하고 신중한 성격
B형: 자유롭고 창의적인 성격
O형: 사교적이고 리더십이 강한 성격
AB형: 이성적이고 독창적인 성격
그 외: "잘못된 입력입니다."

주의: AB형을 먼저 검사해야 합니다! (왜 그런지 생각해보세요)
"""

blood = input("혈액형을 입력하세요 (A/B/O/AB): ")

# 아래에 성격 설명을 출력하세요

# AB형: 이성적이고 독창적인 성격 출력
if blood == "AB":
    print(f"혈액형: {blood}형")
    print("성격: 이성적이고 독창적인 성격")

# A형: 꼼꼼하고 신중한 성격 출력
elif blood == "A":
    print(f"혈액형: {blood}형")
    print("성격: 꼼꼼하고 신중한 성격")

# B형: 자유롭고 창의적인 성격 출력
elif blood == "B":
    print(f"혈액형: {blood}형")
    print("성격: 자유롭고 창의적인 성격")

# O형: 사교적이고 리더십이 강한 성격 출력
elif blood == "O":
    print(f"혈액형: {blood}형")
    print("성격: 사교적이고 리더십이 강한 성격")

# 그 외: "잘못된 입력입니다." 출력
else:
    print("잘못된 입력입니다.")

