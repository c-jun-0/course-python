"""
글로벌시스템융합과 프로그래밍(1) 실습 문제

실습 7: 동전 변환

금액(원)을 입력받아 최소 동전 수로 변환하세요.
사용 가능한 동전: 500원, 100원, 50원, 10원
(10원 미만은 버림)

힌트: 나누기(//)와 나머지(%) 연산자를 사용하세요.
if/elif는 필요 없을 수도 있지만, 각 동전이 0개가 아닐 때만 출력하세요.

[처리 순서]
1. 큰 동전부터 몇 개 필요한지 계산
2. 나머지 금액을 다음 동전으로 계산
3. 0개가 아닌 동전만 출력
"""

amount = int(input("금액을 입력하세요 (원): "))

# 아래에 동전 수를 계산하여 출력하세요

# 동전이 0개 이면 출력 하지 않음
# 입력받은 값에서 500원, 100원, 50원, 10원 출력

# 입력받은 값에서 500원으로 나누기(몫) / 나머지
coin_500 = amount // 500
division_coin500 = amount % 500

if coin_500 > 0:
    print(f"500원: {coin_500}개")

# 나눈 값에서 100원으로 나누기(몫) / 나머지
coin_100 = division_coin500 // 100
division_coin100 = division_coin500 % 100

if coin_100 > 0:
    print(f"100원: {coin_100}개")

# 나눈 값에서 50원으로 나누기(몫) / 나머지
coin_50 = division_coin100 // 50
division_coin50 = division_coin100 % 50

if coin_50 > 0:
    print(f"50원: {coin_50}개")

# 나눈 값에서 10원 동전이 0초과라면 출력
coin_10 = division_coin100 // 10

if coin_10 > 0:
    print(f"10원: {coin_10}개")

# 총 동전 수 출력
result = coin_500 + coin_100 + coin_50 + coin_10
print(f"총 동전 수: {result}개")


