"""
글로벌시스템융합과 프로그래밍(1) 실습 문제

실습 8: 주문 처리

주문 방식(배달/포장)을 입력받아 금액을 계산하세요.
중첩 if문을 사용합니다.
음식 가격은 15000원입니다.

[판정 기준]
- 주문 방식이 "배달"이면:
  - 거리를 입력받는다
  - 거리가 3km 이하: 배달비 2000원
  - 거리가 3km 초과: 배달비 3500원
  - 출력: "배달 주문: 음식 15000원 + 배달비 {배달비}원 = 총 {합계}원"
- 주문 방식이 "포장"이면:
  - 포장 할인 2000원 적용
  - 출력: "포장 주문: 음식 15000원 - 할인 2000원 = 총 13000원"
- 그 외: "잘못된 주문 방식입니다."
"""

order_type = input("주문 방식을 입력하세요 (배달/포장): ")
price = 15000

# 아래에 중첩 if문을 작성하세요

# 배달비 값 초기화
delivery_fee = 0


#  주문 방식이 "배달"이면:
if order_type == "배달":
#    거리를 입력받는다
    distance = int(input("거리를 입력하세요: "))
#    거리가 3km 이하: 배달비 2000원
    if distance <= 3:
        delivery_fee = 2000
#    거리가 3km 초과: 배달비 3500원
    else:
        delivery_fee = 3500
#    출력: "배달 주문: 음식 15000원 + 배달비 {배달비}원 = 총 {합계}원"
    print(f"배달 주문: 음식 {price}원 + 배달비 {delivery_fee}원 = 총 {(price + delivery_fee)}원")
#  주문 방식이 "포장"이면:
elif order_type == "포장":
#    포장 할인 2000원 적용
    discount = 2000
#    출력: "포장 주문: 음식 15000원 - 할인 2000원 = 총 13000원"
    print(f"포장 주문: 음식 {price}원 - 할인 {discount}원 = 총 {(price - discount)}원")
#  그 외: "잘못된 주문 방식입니다."
else:
    print("잘못된 주문 방식입니다.")



