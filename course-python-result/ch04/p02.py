"""
실습 2: 성적 등급

점수를 입력받아 등급을 출력하세요.
if/elif/else를 사용합니다.

[등급 기준]
90 이상: A
80 이상: B
70 이상: C
60 이상: D
60 미만: F
"""

score = int(input("점수를 입력하세요: "))

# 아래에 등급을 판정하여 출력하세요

# 입력받은 점수를 등급으로 변경
# 90 이상이면 A출력
if score >= 90:
    print("등급: A")
# 80 이상이면 B출력
elif score >= 80:
    print("등급: B")
# 70 이상이면 C출력
elif score >= 70:
    print("등급: C")
# 60 이상이면 D출력
elif score >= 60:
    print("등급: D")
# 60 미만이면 F출력
else:
    print("등급: F")

