"""
글로벌시스템융합과 프로그래밍(1) 실습 문제

실습 3: 약수 구하기

숫자를 입력받아 그 수의 약수를 모두 출력하세요.

약수란? 어떤 수를 나누어 떨어지게 하는 수
예: 12의 약수 → 1, 2, 3, 4, 6, 12

힌트: for + if + %(나머지) 연산자를 사용합니다.
"""

n = int(input("숫자를 입력하세요: "))

# 아래에 약수를 출력하세요
for i in range(1,n + 1):
    if n % i == 0:
        print(f"{n}의 약수: {i}")

